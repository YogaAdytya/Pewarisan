public class Karyawan {
    protected String nama;
    protected String idKaryawan;
    
    public void setNama(String nama) {
        this.nama = nama;
    }
    
    public String getNama() {
        return nama;
    }
    
    public void setIdKaryawan(String id) {
        this.idKaryawan = id;
    }
    
    public String getIdKaryawan() {
        return idKaryawan;
    }
}